public class ImplicitConversion {
    public static void main(String[] args) {
        int num = 100 ;
        long irrrgrn = 121633;
        System.out.println(num);
        System.out.println(irrrgrn);
        double d=100.99 ;
        int n=(int)d ;
        System.out.println(d);
        System.out.println(n);
    }
}
