public class Bitlevel {
    public static void main(String[] args) {
        // BITWISE OPERATORS
        int a = 5;  
        int b = 3;
        System.out.println(a & b);  
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~a);
        System.out.println(a << 1);
        System.out.println(a >> 1);

    }
    
}
